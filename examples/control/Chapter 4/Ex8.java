public class Ex8{
    public static void main(String[] args) {
        for(int i=0;i <= 5;i++){
            switch(i){
                case 0:
                System.out.println("switch 0 "+i);
                break;
                case 1:
                System.out.println("switch 1 "+i);
                break;
                case 2:
                System.out.println("switch 2 "+i);
                break;
                case 3:
                System.out.println("switch 3 "+i);
                break;
                case 4:
                System.out.println("switch 4 "+i);
                break;
                default:
                System.out.println("switch default "+i);

            }
        }
    }
}
